export function generateUser(): User {
	console.log('asdf');
	let colorsArray = Array.from(colors.entries());
	let randomIndex: number = Math.floor(Math.random() * colorsArray.length);
	let color = colorsArray[randomIndex];

	randomIndex = Math.floor(Math.random() * animals.length);
	let animal = animals[randomIndex];

	let name = color[0] + animal;

	let newUser: User = {
		senderUsername: name,
		senderColorHex: color[1],
		topic: ''
	};

	return newUser;
}

